#!/usr/bin/env node
/**
 * Import a Google Sheet tab as CSV (File → Download → Comma-separated values)
 * and emit src/data/audit-sheet-rows.json with response-style columns removed.
 *
 * **Links:** For Insert→Link cells, CSV export often drops URLs — prefer:
 *   npm run import-audit-sheet-google
 *
 * Usage:
 *   npm run import-audit-sheet
 *     → uses AUDIT_SHEET_CSV or ~/Downloads/…Master (1).csv then Master.csv
 *   node scripts/import-audit-sheet.mjs /path/to/export.csv
 *
 * Tune excluded headers in scripts/sheet-import.config.json
 */

import { parse } from "csv-parse/sync";
import fs from "fs";
import os from "os";
import path from "path";
import { fileURLToPath } from "url";
import { buildPayloadFromRecords } from "./sheet-import-pipeline.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outPath = path.join(root, "src", "data", "audit-sheet-rows.json");

const MASTER_NAMES = [
  "AU2 Secure Flow + SSC - Simplified Questions - Internal Audit Prep - Template - Master (1).csv",
  "AU2 Secure Flow + SSC - Simplified Questions - Internal Audit Prep - Template - Master.csv",
];

function resolveDefaultCsv() {
  const fromEnv = process.env.AUDIT_SHEET_CSV?.trim();
  if (fromEnv && fs.existsSync(path.resolve(fromEnv))) {
    return path.resolve(fromEnv);
  }
  const dl = path.join(os.homedir(), "Downloads");
  for (const name of MASTER_NAMES) {
    const p = path.join(dl, name);
    if (fs.existsSync(p)) return p;
  }
  return null;
}

let inputPath = process.argv[2]?.trim();
if (!inputPath) {
  inputPath = resolveDefaultCsv();
}
if (!inputPath) {
  console.error(
    "Usage: node scripts/import-audit-sheet.mjs [export.csv]\n" +
      "  Optional: set AUDIT_SHEET_CSV, or place Master CSV in ~/Downloads/\n" +
      "  Or: npm run import-audit-sheet-google",
  );
  process.exit(1);
}

inputPath = path.resolve(inputPath);
console.log("  Reading:", inputPath);

const raw = fs
  .readFileSync(inputPath, "utf8")
  .replace(/^\uFEFF/, "");

const records = parse(raw, {
  columns: true,
  skip_empty_lines: true,
  relax_column_count: true,
  relax_quotes: true,
  trim: true,
});

const rawRecords = records.map((rec) => {
  const out = {};
  for (const [k, v] of Object.entries(rec)) {
    const key = String(k).trim();
    if (!key) continue;
    out[key] = v == null || v === "" ? "" : String(v).trim();
  }
  return out;
});

const { payload, hyperlinkFormulasExpanded, excluded } = buildPayloadFromRecords(
  rawRecords,
  "Generated by scripts/import-audit-sheet.mjs — columns filtered per scripts/sheet-import.config.json.",
);

fs.writeFileSync(outPath, JSON.stringify(payload, null, 2) + "\n", "utf8");
console.log(
  `Wrote ${outPath}\n  Rows: ${payload.rows.length}, columns kept: ${payload.columns.length}`,
);
if (hyperlinkFormulasExpanded > 0) {
  console.log(
    `  HYPERLINK formulas expanded to include URLs: ${hyperlinkFormulasExpanded} cell(s).`,
  );
} else {
  console.log(
    "  No =HYPERLINK() formulas in CSV. If links are missing, use import-audit-sheet-google or paste full https URLs.",
  );
}
console.log("  Excluded headers:", excluded.join("; ") || "(none)");
